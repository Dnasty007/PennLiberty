export function getShape(id: PieceId, rot: number): Shape {
  const list = SHAPES[id];
  return list[((rot % 4) + 4) % 4]!;
}

/** Spawn column for shape origin — centered on a 10-wide well. */
export function spawnX(_id: PieceId): number {
  return 3;
}

export function spawnY(): number {
  return 0; // hidden rows — piece appears dropping into visible field
}
